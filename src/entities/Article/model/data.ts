export function getArticleBySlug(slug: string) {
  return articles.find((article) => article.slug === slug)
}

export function getArticlePath(article: Article) {
  return `/blog/${article.categorySlug}/${article.slug}`
}

export function getArticleByCategoryAndSlug(categorySlug: string, slug: string) {
  return articles.find((article) => article.categorySlug === categorySlug && article.slug === slug)
}
